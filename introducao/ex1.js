/*Escreva um programa que pede o raio de um círculo,
 e em seguida exiba o perímetro e área do círculo.

Para saber o valor do pi em JavaScript, use:
Math.PI */

let raio = 3;
let comprimento = 2 * Math.PI * raio;
let area = Math.PI * raio * raio;

console.log("Comprimento da circunferência é = " + comprimento);
console.log("Area da circunferência é = " + area);

