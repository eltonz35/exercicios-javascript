/*. Você está no Brasil, e para temperatura usamos o grau Celsius.
Porém, quando você for contrato para trabalhar como programador Python 
no exterior, deverá usar graus Fahrenheit.

A fórmula da conversão é a seguinte: F = (9 / 5)* C + 32

Tutorial completo de JavaScript
 Ou seja, você fornece a temperatura em graus Celsius, e seu script JS
 faz a conversão para graus Fahrenheit.
 */

let celsius = 27;
let fahrenheit = (9 / 5) * celsius + 32;

console.log("A temperatura em Fahrenheit é de " + fahrenheit);